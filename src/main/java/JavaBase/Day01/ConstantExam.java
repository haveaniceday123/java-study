package JavaBase.Day01;

public class ConstantExam {
  public static void main(String[] args) {
    // 모두 대문자로 구성된 명사를 사용하는것이 상수의 관례
    final int J;
    // 상수의 장점, 이 값이 뭘 의미하는지 한눈에 알아볼 수 있다.
    final double PI = 3.14159;
    // 상수는 단어와 단어사이를 _로 구분
    final int OIL_PRICE = 1450;

    double circleArea;
    circleArea = 3 * 3 * PI;

  }

}
